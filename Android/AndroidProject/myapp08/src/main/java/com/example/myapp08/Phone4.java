package com.example.myapp08;

public class Phone4 {

    private String name;
    private String Tel;

    public Phone4(String name, String tel) {
        this.name = name;
        this.Tel = tel;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTel() {
        return Tel;
    }

    public void setTel(String tel) {
        Tel = tel;
    }
}
